# Type of numbers

real_number = 3

floating_number = 3.14

# Numbers operation

a = 10
b = 20

# Sum
numbers_sum = a + b
print(f"The sum between {a} and {b} is {numbers_sum}.")
print("----------------------------")

# Difference
numbers_diff = a - b
print(f"The difference between {a} and {b} is {numbers_diff}")
print("----------------------------")

# division
numbers_multiplication = a * b
print(f"The multiplication between {a} and {b} is {numbers_multiplication}")
print("----------------------------")


# Division
numbers_division = a / b
print(f"The division between {a} and {b} is {numbers_division}")
print("----------------------------")


#Operation using int and floating numbers
int_numbers_multiplication = real_number * floating_number

# Multiplication example using int numbers
print(f"The multiplication between {real_number} and {floating_number} is {int(int_numbers_multiplication)}")
print("----------------------------")

# Multiplication example using floating numbers
print(f"The multiplication between {real_number} and {floating_number} is {float(int_numbers_multiplication)}")
print("----------------------------")

